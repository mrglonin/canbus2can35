#!/usr/bin/env python3

def frame(cmd, payload):
    length = len(payload) + 4
    out = [0xFD, length, cmd, *payload]
    checksum = sum(out[1:]) & 0xFFFF
    out += [(checksum >> 8) & 0xFF, checksum & 0xFF]
    return out


def main():
    assert frame(0x05, [0x00]) == [0xFD, 0x05, 0x05, 0x00, 0x00, 0x0A]
    assert frame(0x05, [0x3F]) == [0xFD, 0x05, 0x05, 0x3F, 0x00, 0x49]
    assert frame(0x05, [0x40]) == [0xFD, 0x05, 0x05, 0x40, 0x00, 0x4A]
    assert frame(0x7D, [0x06, 0x00]) == [0xFD, 0x06, 0x7D, 0x06, 0x00, 0x00, 0x89]
    assert frame(0x7D, [0x06, 0x02]) == [0xFD, 0x06, 0x7D, 0x06, 0x02, 0x00, 0x8B]
    print("teyes/raise simulation ok")


if __name__ == "__main__":
    main()
